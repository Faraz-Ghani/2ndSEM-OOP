package pckg.lab2;

//Task#1: Write java program that Declares 5 floating numbers and print their total sum and average.

public class lab2task1 {
public static void main(String[] args) {
	float q,w,e,r,t,y;
	q=1.22f;
	w=3.31f;
	e=4.4f;
	r=1.1f;
	t=0.6f;
	y=q+w+e+r+t;
	System.out.println("Sum of all floating numbers is "+y);
	y=y/5f;
	System.out.println("Average of all floating numbers is "+y);
}
}

